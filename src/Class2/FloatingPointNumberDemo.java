package Class2;

public class FloatingPointNumberDemo {
    public static void main(String[] args) {
         /*
        by default all the floating point numbers are doubles that's why we need to write a f/F at the end of the float
        type variable. but we don't need to worry about it as 99% of the time we will be using a double.
         */
        float grocery=30.5F;   // is a smaller size data type
        System.out.println(grocery);
        double price=11111.22222321321; // is the bigger can hold double the amount
        System.out.println(price);
    }
}
