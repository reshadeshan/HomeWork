package Class6;

public class E6SwitchStatement {
    public static void main(String[] args) {

        String name="Reshad";
        switch (name){
            case "Reshad":
                System.out.println("San Francisco");
                break;
            case "Jenny":
                System.out.println("Chicago");
                break;
            case "Alex":
                System.out.println("Texas");
        }
    }
}
